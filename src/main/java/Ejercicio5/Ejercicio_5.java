public class Ejercicio_5 {
    public void run(){
        System.out.println("******** Ejercicio 5 ********");
        System.out.println("Numero impares while 0 a 100");
        int i=0;
        while (i<=100){
            if (i%2!=0){
                System.out.print(i+" ");
            }
            i++;
        }
        System.out.println("*****************************");
    }
}
